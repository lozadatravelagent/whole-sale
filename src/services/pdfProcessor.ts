/**
 * Extract flight information from PDF text
 */
function extractFlightInfo(text: string): Array<{ airline: string, route: string, price: number, dates: string }> {
    const flights: Array<{ airline: string, route: string, price: number, dates: string }> = [];

    // Patterns for common flight information
    const airlinePatterns = [
        /(?:LATAM|Aerol√≠neas Argentinas|American Airlines|United|Delta|Air France|Iberia|AVIANCA|JetSmart|Flybondi)/gi,
        /(?:AA|UA|DL|AF|IB|AV|LAN|TAM)/gi
    ];

    // Patterns for routes (airport codes or city names)
    const routePatterns = [
        /([A-Z]{3})\s*[-‚Äì‚Üí]\s*([A-Z]{3})/g, // EZE - MIA
        /([A-Z]{3})\s*[-‚Äì‚Üí]\s*([A-Z]{3})\s*[-‚Äì‚Üí]\s*([A-Z]{3})/g, // EZE - MIA - PUJ
        /(Buenos Aires|Madrid|Barcelona|Miami|Punta Cana|Canc√∫n|Nueva York)\s*[-‚Äì‚Üí]\s*(Buenos Aires|Madrid|Barcelona|Miami|Punta Cana|Canc√∫n|Nueva York)/gi
    ];

    // Patterns for dates
    const datePatterns = [
        /(\d{1,2})\/(\d{1,2})\/(\d{4})\s*[-‚Äì]\s*(\d{1,2})\/(\d{1,2})\/(\d{4})/g, // DD/MM/YYYY - DD/MM/YYYY
        /(\d{1,2})\s+(enero|febrero|marzo|abril|mayo|junio|julio|agosto|septiembre|octubre|noviembre|diciembre)\s+(\d{4})\s*[-‚Äì]\s*(\d{1,2})\s+(enero|febrero|marzo|abril|mayo|junio|julio|agosto|septiembre|octubre|noviembre|diciembre)\s+(\d{4})/gi,
        /(\d{4})-(\d{2})-(\d{2})\s*[-‚Äì]\s*(\d{4})-(\d{2})-(\d{2})/g // YYYY-MM-DD - YYYY-MM-DD
    ];

    // Patterns for prices
    const pricePatterns = [
        /(?:USD|US\$|\$)\s*(\d{1,3}(?:,\d{3})*(?:\.\d{2})?)/gi,
        /(\d{1,3}(?:,\d{3})*(?:\.\d{2})?)\s*(?:USD|US\$|\$)/gi
    ];

    // Extract airlines
    const airlines = [];
    for (const pattern of airlinePatterns) {
        const matches = [...text.matchAll(pattern)];
        airlines.push(...matches.map(m => m[0]));
    }

    // Extract routes
    const routes = [];
    for (const pattern of routePatterns) {
        const matches = [...text.matchAll(pattern)];
        routes.push(...matches.map(m => m[0]));
    }

    // Extract dates
    const dates = [];
    for (const pattern of datePatterns) {
        const matches = [...text.matchAll(pattern)];
        dates.push(...matches.map(m => m[0]));
    }

    // Extract prices
    const prices = [];
    for (const pattern of pricePatterns) {
        const matches = [...text.matchAll(pattern)];
        prices.push(...matches.map(m => {
            const priceStr = m[1] || m[0];
            return parseFloat(priceStr.replace(/,/g, ''));
        }));
    }

    // Combine extracted information
    const maxEntries = Math.max(airlines.length, routes.length, dates.length, prices.length, 1);

    for (let i = 0; i < maxEntries; i++) {
        flights.push({
            airline: airlines[i] || 'Aerol√≠nea no especificada',
            route: routes[i] || 'Ruta no especificada',
            price: prices[i] || 0,
            dates: dates[i] || 'Fechas no especificadas'
        });
    }

    console.log('‚úàÔ∏è Extracted flights:', flights);
    return flights.slice(0, 3); // Limit to 3 flights max
}

