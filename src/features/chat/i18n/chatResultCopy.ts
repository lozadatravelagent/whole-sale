export const getTravelerCopy = (language: UserLanguage = 'es') => TRAVELER_COPY[language] || TRAVELER_COPY.es;

export function formatTravelerPhrase(
  travelers: { adults?: number; children?: number; infants?: number } | null | undefined,
  language: UserLanguage = 'es',
): string {
  const copy = getTravelerCopy(language);
  const adults = travelers?.adults ?? 1;
  const children = travelers?.children ?? 0;
  const infants = travelers?.infants ?? 0;
  const parts = [
    copy.adult(adults),
    children > 0 ? copy.child(children) : null,
    infants > 0 ? copy.infant(infants) : null,
  ].filter(Boolean) as string[];
  return parts.join(copy.join);
}

export function formatDateRangePhrase(
  startDate: string | null | undefined,
  endDate: string | null | undefined,
  language: UserLanguage = 'es',
): string {
  const copy = getTravelerCopy(language);
  if (startDate && endDate) return ` ${copy.rangeFromTo(startDate, endDate)}`;
  if (startDate) return ` ${copy.rangeFrom(startDate)}`;
  return '';
}

