export function joinCitiesPhrase(cities: string[], language: UserLanguage = 'es'): string {
  if (cities.length === 0) return '';
  if (cities.length === 1) return cities[0];
  const copy = getPlannerBlockCopy(language);
  if (cities.length === 2) return `${cities[0]}${copy.cityJoinAnd}${cities[1]}`;
  return `${cities.slice(0, -1).join(', ')}${copy.cityJoinAnd}${cities[cities.length - 1]}`;
}

